        }

        private void bbiDelete_ItemClick(object sender, ItemClickEventArgs e)
        {
            if (gridView1.SelectedRowsCount > 0)
            {



                GridView view = gridView1 as GridView;



                int rowHandle = view.GetSelectedRows()[0];

                if (!view.IsGroupRow(rowHandle))
                {
                    RAMSDBModel.UnpavedTentativeRoadMaintenancePlan dw = (RAMSDBModel.UnpavedTentativeRoadMaintenancePlan)view.GetRow(rowHandle);
                    _context.UnpavedTentativeRoadMaintenancePlan.Remove(dw);
                    _context.SaveChanges();
                    view.DeleteRow(rowHandle);

                }
            }
            else
            {
                MessageBox.Show("Please select a row from the table below first.");
            }
        }
