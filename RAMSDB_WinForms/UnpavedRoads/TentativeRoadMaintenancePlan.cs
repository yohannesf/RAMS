        private void GridView_RowValidated(object sender, DevExpress.XtraGrid.Views.Base.ValidateRowEventArgs e)
        {
            GridView view = sender as GridView;
            if (e.Valid)
            {
                if (view.IsNewItemRow(e.RowHandle))
                {
                    RAMSDBModel.UnpavedTentativeRoadMaintenancePlan dw = (RAMSDBModel.UnpavedTentativeRoadMaintenancePlan)view.GetRow(e.RowHandle);
                    _context.UnpavedTentativeRoadMaintenancePlan.Add(dw);
                    _context.SaveChanges();
                }
                else
                {
                    _context.SaveChanges();
                }
            }


        }

