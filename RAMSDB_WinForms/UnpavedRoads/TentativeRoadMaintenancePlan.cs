        private void GridView_UnboundColumn(object sender, DevExpress.XtraGrid.Views.Base.CustomColumnDataEventArgs e)
        {
            GridView view = sender as GridView;
            if (view == null) return;


            int rowIndex = e.ListSourceRowIndex;
            int catID = (int)view.GetListSourceRowCellValue(rowIndex, "CatWorkID");


            if (unitCost.Where(c => c.CatWorkID == catID).Select(c => c.UnpavedUnitCost).FirstOrDefault() == null) return;
            decimal t = (decimal)unitCost.Where(c => c.CatWorkID == catID).Select(c => c.UnpavedUnitCost).FirstOrDefault();


            if (e.Column.FieldName != "UnitCost") return;
            if (e.IsGetData)
            {
                e.Value = t;
            }
        }

